function extractColumnBlock(listContent, constantName) {
  if (!listContent) return null
  const match = listContent.match(new RegExp(`const\\s+${constantName}\\s*=\\s*\\[([\\s\\S]*?)\\]\\s*`, 'm'))
  return match ? match[1] : null
}

function hasTopValueColumnFiltering(contents, constantName) {
  const block = extractColumnBlock(contents.listPage ?? '', constantName)
  if (!block) return false

  const totalColumns = (block.match(/\{\s*id:\s*'/g) ?? []).length
  const lockedColumns = (block.match(/locked:\s*true/g) ?? []).length
  const valueColumns = totalColumns - lockedColumns

  if (valueColumns <= 10) return true

  return block.includes('defaultVisible: false')
}

