function toPascal(value) {
  return value
    .split('-')
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join('')
}

function singularizeSlug(value) {
  const parts = value.split('-').filter(Boolean)
  if (parts.length === 0) return value

  const last = parts[parts.length - 1]
  let singularLast = last

  if (last.endsWith('ies') && last.length > 3) singularLast = `${last.slice(0, -3)}y`
  else if (last.endsWith('ses') && last.length > 3) singularLast = last.slice(0, -2)
  else if (last.endsWith('s') && !last.endsWith('ss') && last.length > 1) singularLast = last.slice(0, -1)

  return [...parts.slice(0, -1), singularLast].join('-')
}

function readTextIfExists(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : null
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true })
}

function writeFileIfMissing(filePath, content, writes) {
  if (fs.existsSync(filePath)) return
  ensureDir(path.dirname(filePath))
  fs.writeFileSync(filePath, content, 'utf8')
  writes.push(filePath)
}

function overwriteGeneratedFileIfNeeded(filePath, content, writes, updates) {
  if (!fs.existsSync(filePath)) return
  const current = fs.readFileSync(filePath, 'utf8')
  if (!current.includes(GENERATED_MARKER)) return
  if (current === content) return
  fs.writeFileSync(filePath, content, 'utf8')
  writes.push(filePath)
  updates.push(filePath)
}

function hasAll(content, patterns) {
  if (!content) return false
  return patterns.every((pattern) => content.includes(pattern))
}

function hasAny(content, patterns) {
  if (!content) return false
  return patterns.some((pattern) => content.includes(pattern))
}

